# Define the generators of the ideals corresponding to delta_11 and delta_21 along which we want to blow up
w_11_1 = q3*y[1]*y[2] + 3*y[5];
w_11_2 = 6*y[1]^4 + 4*q3*y[1]*y[3] + 3*y[6];
w_11_3 = -q3*y[2]^2 + 3*y[8];
w_11_4 = -2*q3*y[1]^2*y[5] - q3*y[2]*y[3] + 3*y[9];
w_11_5 = -4*y[1]^3*y[3] - (1//q3)*y[2]*y[4] + (4//q3)*y[3]^2 + 3*y[10];
# Define the vector used to construct the underlying map of the blowup along the divisor W_delta_11
blowup_vector_11 = [y[1], y[2], y[3], y[4], y[5], y[6], y[7], y[8], y[9], y[10], y[11], y[12], t[1]*w_11_1, t[1]*w_11_2, t[1]*w_11_3, t[1]*w_11_4, t[1]*w_11_5];

w_21_1 = -q3*y[1]*y[2] + 3*y[5];
w_21_2 = 6*y[1]^4 - 4*q3*y[1]*y[3] + 3*y[6];
w_21_3 = q3*y[2]^2 + 3*y[8];
w_21_4 = 2*q3*y[1]^2*y[5] + q3*y[2]*y[3] + 3*y[9];
w_21_5 = -4*y[1]^3*y[3] + q3*y[2]*y[4] - (4//q3)*y[3]^2 + 3*y[10];
# Define the vector used to construct the underlying map of the blowup along the divisor W_delta_21
blowup_vector_21 = [y[1], y[2], y[3], y[4], y[5], y[6], y[7], y[8], y[9], y[10], y[11], y[12], t[1]*w_21_1, t[1]*w_21_2, t[1]*w_21_3, t[1]*w_21_4, t[1]*w_21_5];

# Define the ring C=B[b_1,...,b_5] used to compute the rees algebra of the blowups, which both can be embedded into this relative projective space as the ideals along which we blow up are generated by 5 elements
C, y, b = polynomial_ring(M, :y => (1:12), :b => (1:5));

# Compute the blowup along W_delta_11
blowup_map_11 = hom(C, Bt, blowup_vector_11);
kernel_blowup_map_11 = preimage(blowup_map_11, relations);
basis_blowup_11 = standard_basis(kernel_blowup_map_11, ordering=negdegrevlex(C));  # Get a smaller generating set of the blowup ideal
basis_blowup_ideal_11 = ideal(C, collect(basis_blowup_11));

# Compute the blowup along W_delta_21
blowup_map_21 = hom(C, Bt, blowup_vector_21);
kernel_blowup_map_21 = preimage(blowup_map_21, relations);
basis_blowup_21 = standard_basis(kernel_blowup_map_21, ordering=negdegrevlex(C));  # Get a smaller generating set of the blowup ideal
basis_blowup_ideal_21 = ideal(C, collect(basis_blowup_21));
